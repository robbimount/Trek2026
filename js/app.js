// =============================================================
// Language switcher
// =============================================================

function setLanguage(lang) {
  const t = translations[lang];

  // Update html[lang]
  document.documentElement.lang = lang === 'es' ? 'es-419' : 'en';

  // Swap text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  // Swap alt attributes on images
  document.querySelectorAll('[data-i18n-alt]').forEach(el => {
    const key = el.dataset.i18nAlt;
    if (t[key] !== undefined) {
      el.alt = t[key];
    }
  });

  // Update page title
  document.title = t['page-title'];

  // Update toggle button label and aria-label
  const toggle = document.getElementById('lang-toggle');
  if (toggle) {
    const nextLang = lang === 'en' ? 'es' : 'en';
    toggle.setAttribute('aria-label', lang === 'en' ? 'Cambiar idioma a Español' : 'Switch language to English');
    toggle.dataset.lang = nextLang;
  }

  // Persist choice
  try { localStorage.setItem('trek-lang', lang); } catch (_) {}
}

// Init language from storage or browser preference
(function initLang() {
  let lang = 'en';
  try {
    const stored = localStorage.getItem('trek-lang');
    if (stored === 'es' || stored === 'en') {
      lang = stored;
    } else if (navigator.language && navigator.language.startsWith('es')) {
      lang = 'es';
    }
  } catch (_) {}
  if (lang === 'es') setLanguage('es');
})();

// Toggle click
const langToggle = document.getElementById('lang-toggle');
if (langToggle) {
  langToggle.addEventListener('click', () => {
    const next = langToggle.dataset.lang || 'es';
    setLanguage(next);
  });
}

// =============================================================
// Smooth scroll for all anchor links
// =============================================================

document.querySelectorAll('a[href^="#"]').forEach(anchor => {
  anchor.addEventListener('click', function (e) {
    const target = document.querySelector(this.getAttribute('href'));
    if (target) {
      e.preventDefault();
      target.scrollIntoView({ behavior: 'smooth', block: 'start' });

      // Close mobile nav if open
      const navLinks = document.getElementById('nav-links');
      const menuToggle = document.getElementById('menu-toggle');
      if (navLinks && navLinks.classList.contains('nav-open')) {
        navLinks.classList.remove('nav-open');
        menuToggle.setAttribute('aria-expanded', 'false');
      }
    }
  });
});

// =============================================================
// Sticky nav shadow on scroll
// =============================================================

const header = document.getElementById('site-header');
if (header) {
  window.addEventListener('scroll', () => {
    header.classList.toggle('nav-scrolled', window.scrollY > 10);
  }, { passive: true });
}

// =============================================================
// Mobile nav toggle
// =============================================================

const menuToggle = document.getElementById('menu-toggle');
const navLinks = document.getElementById('nav-links');
if (menuToggle && navLinks) {
  menuToggle.addEventListener('click', () => {
    const isOpen = navLinks.classList.toggle('nav-open');
    menuToggle.classList.toggle('menu-open', isOpen);
    menuToggle.setAttribute('aria-expanded', String(isOpen));
  });

  // Close nav when clicking a link (already handled for smooth scroll, but covers external links too)
  navLinks.addEventListener('click', e => {
    if (e.target.closest('a')) {
      navLinks.classList.remove('nav-open');
      menuToggle.classList.remove('menu-open');
      menuToggle.setAttribute('aria-expanded', 'false');
    }
  });
}

// =============================================================
// Scroll-triggered fade-in animations
// =============================================================

if ('IntersectionObserver' in window) {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  document.querySelectorAll('.animate-on-scroll').forEach(el => observer.observe(el));
}

// =============================================================
// Back-to-top button
// =============================================================

const backToTop = document.getElementById('back-to-top');
if (backToTop) {
  window.addEventListener('scroll', () => {
    backToTop.classList.toggle('visible', window.scrollY > 400);
  }, { passive: true });

  backToTop.addEventListener('click', () => {
    window.scrollTo({ top: 0, behavior: 'smooth' });
  });
}
